def make(id_, name, name_en, birth, death, country, field, wiki, summary):
    return {
        'id': id_,
        'name': name,
        'nameEn': name_en,
        'birth': birth,
        'death': death,
        'country': country,
        'field': field,
        'summary': summary,
        'wikiTitle': wiki,
        'events': [],
        'quotes': [],
        'places': [],
        'imageUrl': '',
        'books': [],
        'media': [],
        'works': [],
        'routine': {},
        'lifeDigest': '',
        'traits': {},
        'relations': [],
    }

def main():
    existing = {fp.stem for fp in BASE.glob('*.json')}
    added, skipped = 0, 0
    for tup in PEOPLE:
        id_ = tup[0]
        if id_ in existing:
            print(f'  skip (exists): {id_}')
            skipped += 1
            continue
        fp = BASE / f'{id_}.json'
        doc = make(*tup)
        fp.write_text(json.dumps(doc, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')
        print(f'  added: {id_:25s} {tup[1]}')
        added += 1
    print(f'\n===\nAdded: {added}人  / Skipped: {skipped}人')

if __name__ == '__main__':
    main()

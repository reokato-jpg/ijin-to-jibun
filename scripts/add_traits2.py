def main():
    added = 0
    for pid, traits in T.items():
        path = PEOPLE / f"{pid}.json"
        if not path.exists():
            print(f"skip: {pid} (file missing)")
            continue
        p = json.loads(path.read_text(encoding="utf-8"))
        if p.get("traits"):
            print(f"skip: {pid} (already has traits)")
            continue
        p["traits"] = traits
        path.write_text(json.dumps(p, ensure_ascii=False, indent=2), encoding="utf-8")

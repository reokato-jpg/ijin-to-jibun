count = 0
for era_id, echoes in echoes_data.items():
    # Build the echoes JS array
    echoes_js = '[\n      ' + ',\n      '.join(
        '{ chain: \'' + e['chain'].replace("'", "\\'") + '\', pattern: \'' + e['pattern'].replace("'", "\\'") + '\'' +
        (', body: \'' + e['body'].replace("'", "\\'") + '\'' if e.get('body') else '') + ' }'
        for e in echoes
    ) + ',\n    ]'

    # Find era block
    pattern = r"(\s*)(" + re.escape(era_id) + r"):\s*\{"
    m = re.search(pattern, content)
    if not m:
        print('NOT FOUND:', era_id)
        continue
    depth = 1
    i = m.end()
    while i < len(content) and depth > 0:
        if content[i] == '{':
            depth += 1
        elif content[i] == '}':
            depth -= 1
        i += 1
    era_end = i
    close_brace = era_end - 1
    insertion = '\n    echoes: ' + echoes_js + ','
    content = content[:close_brace] + insertion + content[close_brace:]
    count += 1

with open(path, 'w', encoding='utf-8') as f:
    f.write(content)
print(f'Added echoes to {count} eras')

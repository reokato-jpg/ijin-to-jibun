def main():
    added = 0
    for pid, routine in ROUTINES.items():
        path = PEOPLE / f"{pid}.json"
        if not path.exists():
            continue
        p = json.loads(path.read_text(encoding="utf-8"))
        if p.get("routine") and len(p["routine"]) > 3:
            continue  # 既にあればスキップ
        p["routine"] = routine
        path.write_text(json.dumps(p, ensure_ascii=False, indent=2), encoding="utf-8")
        added += 1
        print(f"{pid}: ルーティン追加")
    print(f"\n合計 {added} 人に追加")


if __name__ == "__main__":
    main()
